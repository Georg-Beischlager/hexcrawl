      <h2 class="mt-8">
        Was ist eine Session?
      </h2>
      <p>Eine Session ist ein Treffen (zB in Peron, oder online) wo wir mit dem Ziel die Geschichte fortzuführen zusammenkommen.</p>
      <h2 class="mt-8">
        Warum soll ich eine Session leiten?
      </h2>
      <p>
        Wenn du Freude daran hast dir Geschichten auszumalen, wenn du gerne überlegst was in der Welt sinnvoll oder dramatisch wäre.
        Wenn du gerne sehen willst wie es auf der anderen Seite des GM-Screens ist.<br>
        Einer der wenigen schlechten Gründe ist wenn du mächtig sein willst, oder um jeden Preis "gewinnen" willst.
        Ein GM gewinnt wenn alle Spaß haben (inkl des GMS selbst).
      </p>
      <h2 class="mt-8">
        Was darf ich in die Welt einbringen?
      </h2>
      <h3>
        Alles was nicht explizit verboten ist, ist erlaubt.<br>
        Grundsätzlich gelten zwei Regeln:
      </h3>
      <ol>
        <li>Respektiere den Beitrag anderer</li>
        <li>Respektiere die Charaktere</li>
      </ol>
      <p class="mt-4">
        Wenn du etwas in die Welt einbringst, denke daran den Spaß/die Idee der anderen nicht zu zerstören. Das beinhält folgende Dinge:
      </p>
      <ul>
        <li>Vernichte etwas dass andere sich überlegt haben nicht direkt. Wenn jemand eine Gebirgskette einbaut, sprenge sie nicht. Wenn jemand eine Eiswüste baut, taue sie nicht auf.</li>
        <li>Wenn ein Konflikt in der Welt herrscht, löse ihn nicht durch Zauberhand. Probleme, Konflikte und Streits sind im Kern einer Geschichte, lasse die Leute die sie einführen diese auch lösen (oder nicht).</li>
        <li>Dazu zählen auch zB: zu fortgeschrittene Technologien, zu üppige Rohstoffe, der Zwang weit weg zu fliehen usw.</li>
        <li>Ausnahmen gibt es immer, wenn du etwas drastisches machen willst, tu dich mit denen zusammen die es erschaffen haben.</li>
      </ul>

